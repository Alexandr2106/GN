<p>Сообщается, что GSC Game World возобновила работу над Stalker 2: Heart of Chornobyl. 2 марта разработчик объявил, что приостанавливает работу над игрой. В том же месяце студия перевела своих сотрудников в Прагу.</p>
<p>Теперь, похоже, игра вернулась в нужное русло. Отвечая на вопрос члена сообщества Discord Stalker 2 о том, возобновилась ли разработка, сотрудник GSC Game World сказал: «Это продолжается», - в сообщении, опубликованном польским игровым изданием GRYOnline.</p>
<p>После задержки в начале года GSC Game World заявила, что выпустит Stalker 2 8 декабря. Объявление было сделано до спецоперации. С тех пор, компания изменила название игры. Её подзаголовок теперь гласит «Сердце Чернобыля» вместо «Чернобыль».</p>

