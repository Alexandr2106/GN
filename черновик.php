<p>
    К апрелю компания предоставит инструкцию по переносу библиотеки в Steam.
    Для тех у кого могли появится вопросы, подготовили <a href="" target="_blank">подробную статью с ответами на них</a>.
    Возможность запускать игры с помощью программы запуска Bethesda.net сохранится до начала мая, но рекомендуется вам выполнить перенос, как только он будет доступен.
</p>

<p>
    Epic Games в своём твиттер-аккаунте сообщила, что прекращает торговлю с Россией в собственных играх, но пока не уточнила, чего именно это коснётся. Решение в компании приняли из-за продолжающегося вооружённого конфликта в Украине.
</p>

