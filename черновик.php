<p>Разработчики перезапуска Saints Row опубликовали 18 минут геймплея экшена. Большую часть ролика посвятили богатой кастомизации персонажа.</p>
<p>Предстоящая компьютерная игра в жанре action-adventure, разрабатываемая Volition и издаваемая Deep Silver. Это пятая часть в серии Saints Row, являющаяся перезагрузкой всей франшизы.</p>

