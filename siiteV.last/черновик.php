<p>
    Пользователи смогут перенести в Steam всю свою библиотеку игр и кошелек. Данные учетной записи Bethesda.net не потеряются. Для многих продуктов также будет осуществлен перенос сохраненных игр. Учетная запись Bethesda.net не будет удалена, ёё можно будет использовать на веб-сайте Bethesda.net и в играх.
