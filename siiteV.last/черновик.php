Игры Ubisoft, Microsoft и Rockstar стали не доступны в российском Steam

<p>Из Steam для российских пользователей стали не доступны для приобретения игры издателей, приостановивших свою деятельность на территории страны из-за ситуации на Украине. Пользователи уже не могут купить Gears 5, игры серии Far Cry и Assassin's Creed и другие проекты Ubisoft, Microsoft, Rockstar и Take-Two. Однако если они уже были куплены, то из библиотеке не исчезают.</p>
<img src="https://i.yapx.ru/RFTia.jpg" alt="Игры Ubisoft, Microsoft и Rockstar стали не доступны в российском Steam">

<p>Небольшой список интересных проектов, заблокированных в России:</p>
<ul>
    <li>New World</li>
    <li>For Honnor</li>
    <li>Dead by Daykight</li>
    <li>The Witcher</li>
    <li>Cyberpunk 2077</li>
    <li>Rainbow Six</li>
    <li>DOOM</li>
</ul>
<p> Похоже, данный список будет постепенно дополнятся. Кстати, нем пока нет игр EA, которая также обещала приостановить продажи своих проектов в России.</p>

<p>Посмотреть полный список игр, которые пропадают для российских пользователей, можно на <a href="https://steamdb.info/history/?filterkey=535">SteamDb</a>.</p>

